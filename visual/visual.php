      const cubeMovement = function (top, cubeSet, cubePos) {
        for (var i = 0; i < cubeSet.length; i++) {
          let posX = cubePos[i][0] * top;
          let posY = cubePos[i][1] * top;

          if (cubePos[i][0] > 0) {
            if (posX < 0) {
              posX = 0;
            }
          }

          if (cubePos[i][0] < 0) {
            if (posX > 0) {
              posX = 0;
            }
          }

          if (cubePos[i][1] > 0) {
            if (posY < 0) {
              posY = 0;
            }
          }

          if (cubePos[i][1] < 0) {
            if (posY > 0) {
              posY = 0;
            }
          }

          cubeSet[i].style.cssText += `transform: translate(${posX}px, ${posY}px)`;
        }
      };

      window.addEventListener('scroll', function () {
        const cubesA = this.document.querySelectorAll('[data-depth="0"] .visual-box');
        const cubesB = this.document.querySelectorAll('[data-depth="1"] .visual-box');
        const cubesC = this.document.querySelectorAll('[data-depth="2"] .visual-box');
        const cubesD = this.document.querySelectorAll('[data-depth="3"] .visual-box');

        const height = window.innerHeight;
        const parallaxHeight = this.document.querySelector('.parallax_scroll').clientHeight;
        let top =
          (this.document.querySelector('.subcontent').getBoundingClientRect().top -
            height) /
          parallaxHeight;

        cubeMovement(top, cubesA, cubePosDepthA);
        cubeMovement(top, cubesB, cubePosDepthB);
        cubeMovement(top, cubesC, cubePosDepthC);
        cubeMovement(top, cubesD, cubePosDepthD);

        const simbolParallaxHeight =
          this.document.querySelector('.subcontent').clientHeight;
        let simbolTop =
          this.document.querySelector('.subcontent2').getBoundingClientRect().top /
          simbolParallaxHeight;

        const simbol = this.document.querySelector('.visual-logo');

        if (simbolTop < 0) {
          simbol.style.cssText += `opacity:${1 + simbolTop}`;
          this.document.querySelector('.visual-container').style.display = 'none';
        } else {
          simbol.style.cssText += `opacity:${1 - simbolTop}`;
          this.document.querySelector('.visual-container').style.display = 'block';
        }
      });

