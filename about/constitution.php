                                <h4 class="sec mt0">제 16 절 의원총회</h4>

                                <h4>제 53 조 (구성) </h4>
                                <p>원내 최고 의사결정기구로 당 소속 국회의원으로 구성되는 의원총회를 둔다.</p>

                                <h4>제 54 조 (의원의 의무와 지위) </h4>
                                <p class="ind">① 의원은 국민의 대표자로서 헌법이 부여한 직무와 선서한 의무를 성실히 수행하여야 한다.</p>
                                <p class="ind">② 당의 모든 기구는 의원의 지위와 권한을 최대한 존중하고 보장하여야 한다.</p>

                                <h4>제 55 조 (기능) </h4>
                                <p class="ind">① 의원총회는 의원의 최고의사결정기구이다.</p>
                                <p class="ind">
                                    ② 의원총회는 다음의 기능을 가진다.<br/>
                                    1. 원내대표의 선출<br/>
                                    2. 국회의장단 및 국회상임위원회 위원장 후보자 선출<br/>
                                    3. 제22조제3항제4호에 의한 상임전국위원의 선임<br/>
                                    4. 국회대책 및 원내전략의 결정<br/>
                                    5. 국가 주요정책 및 주요법안의 심의<br/>
                                    6. 국회제출 법안 및 의안 중 주요쟁점사안의 심의‧의결<br/>
                                    7. 당무에 관한 의견 개진 및 보고 청취<br/>
                                    8. 국회의원의 제명에 관한 사항<br/>
                                    9. 기타 당헌 또는 당규가 정하는 사항과 최고위원회의가 회부하는 사항의 처리
                                </p>
                                <p class="ind">③ 국회의장‧부의장 및 국회상임위원회 위원장 후보자 선출방법에 관한 기타 필요한 사항은 당규로 정한다.</p>

                                <h4>제 56 조 (의장) </h4>
                                <p>
                                    원내대표는 의원총회의 의장이 되고, 의장이 회의를 주재할 수 없을 경우에는 다음 각 호의 순서에 따라 회의를 주재한다.<br/>
                                    1. 원내수석부대표<br/>
                                    2. 원내부대표 중 1인
                                </p>

                                <h4>제 57 조 (소집) </h4>
                                <p class="ind">① 의원총회는 월 2회 정기적으로 소집함을 원칙으로 하며, 의원총회의 의결로 달리할 수 있다.</p>
                                <p class="ind">② 의원총회는 원내대표가 필요하다고 인정하거나 재적의원 10분의 1 이상의 요구 또는 최고위원회의의 요청이 있을 때 원내대표가 소집하여야 한다.</p>
                                <p class="ind">③ 의장은 소집 목적과 구체적인 안건을 명시하여 최소한 48시간 전에 소속 의원에게 서면으로 통보하여야 한다. 다만, 긴급을 요하는 경우에는 예외로 한다.</p>
                                <p class="ind">④ 의장은 소속 의원들의 요청시 원내상황 등 의정활동에 필요한 정보를 신속하게 제공하고 설명하여야 한다.</p>

                                <h4>제 58 조 (회의) </h4>
                                <p class="ind">① 의원총회는 공개를 원칙으로 한다. 다만, 원내대표 또는 출석의원 10인 이상의 요구가 있을 때에는 출석의원 과반수의 찬성으로 공개하지 아니할 수 있다.</p>
                                <p class="ind">② 의장은 의원총회의 회의록을 작성‧보관하여야 한다.</p>
                                <p class="ind">③ 의원은 10인 이상의 동의로 안건을 추가하거나 수정안을 제출할 수 있다.</p>
                                <p class="ind">④ 안건에 대한 충분한 정보제공과 관련분야 의견수렴을 위하여 외부전문가 토론, 패널토론, 청문회 등 다양한 토론 방식을 활용할 수 있다.</p>

                                <h4>제 59 조 (의결정족수) </h4>
                                <p class="ind">
                                    ① 의원총회의 의결은 거수 혹은 기립을 원칙으로 하며, 재적의원 과반수의 출석과 출석의원 과반수의 찬성으로 의결한다. 다만, 의장이
                                    필요하다고 인정하거나, 재적의원 3분의 1 이상의 요구가 있을 때에는 직접, 비밀 투표로 의결하며, 이 경우에는 반드시 의원에게 사전에 문서로 통지하되, 야간 기타
                                    부득이한 경우에는 그러하지 아니하다.
                                </p>
                                <p class="ind">② 재적의원 3분의 1의 요구가 있는 특별 안건의 경우에는 재적의원 2분의 1 이상의 찬성으로 의결한다.</p>
                                <p class="ind">③ 당론변경, 헌법개정, 대통령탄핵, 국회의원의 제명은 재적의원 3분의 2 이상의 찬성으로 의결한다.</p>
                                <p class="ind">④ 의결사안과 이해관계가 있는 자는 그 회의체에서 당연 제척된다.</p>
                                <p class="ind">⑤ 당헌상 달리 의결정족수와 관련한 규정이 없는 경우 이 조항을 준용한다.</p>

                                <h4>제 60 조 (양심에 따른 투표의 자유) </h4>
                                <p class="ind">① 의원은 헌법과 양심에 따라 국회에서 투표할 자유를 가진다.</p>
                                <p class="ind">② 제59조에 따라 의원총회에서 의결한 당론에 대하여 의원이 국회에서 그와는 반대되는 투표를 했을 경우에 의원총회는 의결로서 그에 대한 소명을 들을 수 있다.</p>
                                <p class="ind">③ 생명, 윤리, 종교와 관련된 사안에 대하여는 당론을 정하지 아니한다.</p>

                                <h4 class="sec">제 17 절 원내대표</h4>
                                <h4>제 61 조 (지위) </h4>
                                원내대표는 교섭단체 대표로서 국회운영에 관한 책임과 최고 권한을 갖는다.

                                <h4>제 62 조 (선출 및 임기)</h4>
                                <p class="ind">① 원내대표는 의원총회에서 선출하며, 그 선출방법에 관한 기타 필요한 사항은 당규로 정한다.</p>
                                <p class="ind">② 원내대표의 임기는 1년으로 한다.</p>

                                <h4>제 63 조 (권한) </h4>
                                <p class="ind">
                                    ① 원내대표는 다음의 권한을 가진다.<br/>
                                    1. 의원총회 및 원내대책위원회의 주재<br/>
                                    2. 소속 국회의원의 상임위원회 등에 대한 배정<br/>
                                    3. 원내수석부대표 및 원내부대표의 추천과 임명<br/>
                                    4. 정책위원회 부의장, 정책조정위원장 및 부위원장, 위원의 임명<br/>
                                    5. 기타 국회운영에 필요한 사항의 처리
                                </p>
                                <p class="ind">② 제1항제2호의 권한행사시에는 정책위원회 의장의 의견을 들어 배정한다.</p>

                                <h4>제 64 조 (원내부대표 등) </h4>
                                <p class="ind">① 원내대표를 보좌하기 위하여 원내수석부대표를 포함한 15인 이내의 원내부대표를 둔다.</p>
                                <p class="ind">② 원내부대표는 원내대표의 추천과 의원총회의 의결을 거쳐 원내대표가 임명한다.</p>
                                <p class="ind">③ 원내대표가 사고로 인하여 직무를 수행할 수 없을 때에는 원내수석부대표가 그 직무를 대행한다.</p>

                                <h4>제 65 조 (원내대책위원회) </h4>
                                <p class="ind">① 국회활동에 관한 당의 주요대책을 협의·조정하기 위하여 원내대책위원회를 둔다.</p>
                                <p class="ind">② 원내대책위원회는 원내대표, 정책위원회 의장, 국회 상임위원회 위원장 및 간사, 원내수석부대표 및 원내부대표, 정책위원회 부의장, 정책조정위원장으로 구성한다.</p>
                                <p class="ind">③ 원내대책위원회의 위원장은 원내대표가 겸한다.</p>
                                <p class="ind">④ 원내대표가 사고로 인하여 직무를 수행할 수 없을 때에는 제64조(원내부대표 등) 제3항의 규정에도 불구하고 정책위원회 의장이 그 직무를 대행한다.</p>
                                <p class="ind">⑤ 원내대책위원회에 당규가 정하는 바에 따라 원내기획실 및 필요부서를 둔다.</p>
                                <p class="ind">⑥ 원내대책위원회에 관한 기타 필요한 사항은 당규로 정한다.</p>

                                <h4 class="sec">제 18 절 정책위원회</h4>

                                <h4>제 66 조 (구성) </h4>
                                <p class="ind">① 당 정책의 입안, 심의 및 현안대처 기관으로서 의원총회 산하에 정책위원회를 둔다.</p>
                                <p class="ind">② 정책위원회에 정책위원회 의장과 정책위원회 부의장, 정책조정위원장 및 부위원장, 위원을 둘 수 있다.</p>
                                <p class="ind">③ 정책위원회에 관한 기타 필요한 사항은 당규로 정한다.</p>
                                <p class="ind">④ 정책위원회에 당규가 정하는 바에 따라 필요부서를 둔다.</p>
                                <p class="ind">⑤ 정책위원회 의장은 최고위원회의 또는 원내대책위원회의 동의를 얻어 필요한 특별기구를 둘 수 있다.</p>

                                <h4>제 67 조 (기능) </h4>
                                <p class="ind">
                                    ① 정책위원회는 다음의 기능을 가진다.<br/>
                                    1. 당 정책의 연구·심의 및 입안<br/>
                                    2. 정부정책에 대한 검토 및 대안제시<br/>
                                    3. 법률안, 대통령령안, 예산안, 국민생활 또는 국가경제에 중대한 영향을 미치는 정책안에 대한 당‧정 정책협의 또는 검토 업무<br/>
                                    4. 의원입법안의 연구 및 심의<br/>
                                    5. 당 정책에 관한 자문사항의 심의<br/>
                                    6. 주요 사회단체의 정책현안 수렴 및 정책 연계활동 등 당내‧외 여론 수렴을 통한 정책 개발<br/>
                                    7. 제1호 내지 제5호의 사항에 관한 대외홍보
                                </p>
                                <p class="ind">② 정책위원회에서 심의된 제1항제1호 및 제2호는 원내대책위원회의 의결을 거쳐야 한다.</p>
                                <p class="ind">③ 정책위원회 의장은 제1항제3호의 결과를 의원총회에서 소속 의원에게 보고하여야 한다.</p>

                                <h4>제 68 조 (정책위원회 의장 등) </h4>
                                <p class="ind">① 정책위원회 의장은 정책위원회를 대표하며, 회무를 총괄한다.</p>
                                <p class="ind">
                                    ② 정책위원회 의장은 다음의 권한을 갖는다.<br/>
                                    1. 정책위원회의 주재<br/>
                                    2. 당 정책에 관한 협의‧조정<br/>
                                    3. 당정협의업무 총괄‧조정<br/>
                                    4. 정책위원회 부의장 및 정책조정위원장 및 부위원장, 위원의 추천
                                </p>
                                <p class="ind">③ 정책위원회 의장은 당 대표가 원내대표와의 협의를 거쳐 의원총회의 추인을 받아 임명한다. 의원총회는 재적의원 과반수의 출석과 출석의원 과반수의 찬성으로 의결한다.</p>
                                <p class="ind">④ 정책위원회 의장의 임기는 1년으로 한다.</p>
                                <p class="ind">⑤ 정책위원회 의장은 정책조정위원회간 정책조정을 위하여 약간인의 부의장을 둘 수 있으며, 부의장은 정책위원회 의장의 추천으로 의원총회 의결을 거쳐 원내대표가
                                임명한다.</p>
                                <p class="ind">⑥ 정책조정위원장은 정책위원회 의장의 추천으로 원내대표가 임명한다.</p>
                                <p class="ind">⑦ 정책위원회 의장이 사고로 인하여 직무를 수행할 수 없을 때에는 정책위원회 의장이 지명하는 부의장이 그 직무를 대행한다.</p>
