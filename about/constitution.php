                                    9. 당의 주요 정책에 대한 보고 청취 및 기타 당무운영에 관한 주요사항과 최고위원회의가 회부하는 사항의 심의‧처리
                                </p>
                                <p class="ind">② 상임전국위원회에 관한 기타 필요한 사항은 당규로 정한다.</p> 

                                <h4>제 24 조 (소집 및 의결정족수) </h4>
                                <p class="ind">① 상임전국위원회는 정기회의와 임시회의로 구분한다.</p>
                                <p class="ind">② 정기회의는 월 2회 소집함을 원칙으로 하고, 임시회의는 최고위원회의의 의결 또는 재적위원 4분의 1 이상의 요구 및 긴급 현안이 발생하였다고 의장이 인정할 때 의장이 소집한다.</p>
                                <p class="ind">③ 상임전국위원회 의결정족수는 제59조를 준용한다.</p> 

                                <h4 class="sec">제 4 절 당 대표 및 최고위원</h4> 

                                <h4>제 25 조 (지위와 권한) </h4>
                                <p class="ind">① 당 대표는 법적‧대외적으로 당을 대표하고 당무를 통할한다.</p>
                                <p class="ind">
                                    ② 당 대표는 당내 소통확대 및 원활한 당무수행을 위하여 정례적으로 다음 각 호의 회의를 개최한다.<br/>
                                    1. 중앙당 주요당직자, 각종 위원회 위원장이 참석하는 확대당직자회의<br/>
                                    2. 당 대표 등 중앙당 주요당직자가 참석하는 원외당원협의회 운영위원장 회의
                                </p>
                                <p class="ind">③ 당 대표는 원활한 당무수행을 위하여 필요시 당직자회의를 소집할 수 있으며, 특별위원회를 구성할 수 있다. 다만, 특별위원회 구성 시 최고위원회의의 추인을 받아야 한다.</p>
                                <p class="ind">④ 당 대표는 당직자 인사에 관하여 임면권 및 추천권을 가진다.</p>
                                <p class="ind">⑤ 당 대표의 권한에 관한 기타 필요한 사항은 당규로 정한다.</p> 

                                <h4>제 26 조 (당 대표의 선출) </h4>
                                <p class="ind">① 당 대표는 당 대표 선출을 위한 선거인단이 실시한 선거와 여론조사에서 최다득표한 자로 선출하여 전당대회에서 지명한다.</p>
                                <p class="ind">② 당 대표 당선자를 결정함에 있어 당 대표 선출을 위한 선거인단의 유효투표결과를 70%, 여론조사결과를 30% 반영한다. 다만, 후보자로 등록한 자가 1인이거나 선거기간 중 중도사퇴 등의 사유로 후보자가 1인이 된 경우에는 당 대표 및 최고위원 선출을 위한 선거관리위원회의 결정에 따라 후보자에 대한 투표를 실시하지 아니하고 전당대회에서 그 후보자를 당 대표로 지명할 수 있다.</p>
                                <p class="ind">
                                    ③ 당 대표의 궐위 또는 기타의 사유로 인하여 당 대표 선출의 사유가 발생한 때에는 다음의 각호에 따른다.<br/>
                                    1. 궐위된 당 대표의 잔여임기가 6개월 미만일 경우에는 원내대표가 그 직을 승계한다. 다만, 원내대표 유고시에는 새로운 원내대표 선출시까지 선출직 최고위원 선거 득표순으로 승계한다.<br/>
                                    2. 궐위된 당 대표의 잔여임기가 6개월 이상일 경우에는 궐위된 날로부터 60일 이내에 임시전당대회를 개최하여 제1항 및 제2항에 따라 다시 선출된 당 대표를 지명하여야 한다.
                                </p>
                                <p class="ind">④ 제3항제1호 및 제2호의 임기는 전임자의 잔여임기로 한다.<p>
                                <p class="ind">⑤ 당 대표의 선출에 관한 기타 필요한 사항은 당규로 정한다.</p> 

                                <h4>제 27 조 (선출직 최고위원) </h4>
                                <p class="ind">① 선출직 최고위원은 최고위원 선출을 위한 선거인단이 실시한 선거와 여론조사에서 1위 내지 4위의 득표자로 선출하여 전당대회에서 지명한다. 다만, 4위 득표자 이내에 여성당선자가 없을 경우에는 4위 득표자 대신 여성후보자 중 최다득표자를 최고위원으로 한다.</p>
                                <p class="ind">② 선출직 최고위원 선거의 후보자가 4명 미만일 경우, 잔여정원은 전국위원회의의 의결을 거쳐 당 대표가 임명한다.</p>
                                <p class="ind">③ 선출직 최고위원이 궐위 시에는 그 사유가 발생한 날로부터 30일 이내에 전국위원회에서 최고위원을 선출하며, 그 임기는 전임자의 잔여임기로 한다. 다만, 잔여임기가 2개월 미만일 때에는 그러하지 아니한다.</p>
                                <p class="ind">④ 선출직 최고위원 당선자를 결정함에 있어 최고위원 선출을 위한 선거인단의 유효투표결과를 70%, 여론조사결과를 30% 반영한다.</p> 

                                <h4>제 27 조의 2 (청년최고위원) </h4>
                                <p class="ind">① 청년최고위원은 최고위원 선출을 위한 선거인단이 실시한 선거와 여론조사에서 최다득표자로 선출하여 전당대회에서 지명한다.</p>
                                <p class="ind">② 청년최고위원 당선자를 결정함에 있어 최고위원 선출을 위한 선거인단의 유효투표결과를 70%, 여론조사결과를 30% 반영한다. 다만, 후보자로 등록한 자가 1인이거나 선거기간 중 중도사퇴 등의 사유로 후보자가 1인이 된 경우에는 당 대표 및 최고위원 선출을 위한 선거관리위원회의 결정에 따라 후보자에 대한 투표를 실시하지 아니하고 전당대회에서 그 후보자를 청년최고위원으로 지명할 수 있다.</p>
                                <p class="ind">③ 청년최고위원 선거의 입후보자가 없을 경우, 전국위원회의 의결을 거쳐 당 대표가 임명한다.</p>
                                <p class="ind">④ 청년최고위원이 궐위된 때에는 그 사유가 발생한 날로부터 30일 이내에 전국위원회에서 청년최고위원을 선출하며, 그 임기는 전임자의 잔여임기로 한다. 다만, 잔여임기가 2개월 미만일 때에는 그러하지 아니하다.</p>

                                <h4>제 27 조의 3 (선거인단) </h4>
                                <p>당 대표 및 최고위원 선출 선거인단의 구성에 관한 사항은 당규로 정한다. </p>

                                <h4>제 28 조 (임기) </h4>
                                <p>당 대표와 최고위원의 임기는 2년으로 한다. </p>

                                <h4>제 29 조 (권한대행) </h4>
                                <p>당 대표가 궐위된 경우, 당 대표가 선출되기 전까지는 원내대표, 선출직 최고위원 중 최고위원 선거 득표순으로 그 권한을 대행한다. 다만, 권한을 대행할 원내대표, 선출직 최고위원이 없는 경우에는 최다선 의원이 대행하고 최다선 의원이 2명 이상인 경우에는 연장자 순으로 그 권한을 대행한다.</p>

                                <h4>제 29 조의 2 (직무대행) </h4>
                                <p>당 대표가 사고 등으로 인하여 직무를 수행할 수 없을 때에는 원내대표, 최고위원 중 최고위원 선거 득표순으로 그 직무를 대행한다.</p>

                                <h4>제 30 조 (자문 및 보좌기관) </h4>
                                <p class="ind">① 당무에 관한 당 대표의 자문기관으로 당 원로 및 사회지도급 인사 중에서 최고위원회의의 협의를 거쳐 당 대표가 상임고문을 위촉할 수 있다.</p>
                                <p class="ind">② 대통령후보출마예정자는 최고위원회의의 협의를 거쳐 당 대표가 상임고문으로 위촉하고 상임고문이 된 대통령후보출마예정자는 각종 회의에 참석하여 당무 전반에 관한 자신의 의견을 개진할 수 있다.</p>
                                <p class="ind">③ 당 대표를 보좌하기 위하여 당 대표가 임명하는 당 대표 비서실장과 특별보좌역을 둘 수 있다.</p>
                                <p class="ind">④ 당 대표의 자문 및 보좌기관에 관한 기타 필요한 사항은 당규로 정한다.</p>

                                <h4 class="sec">제 5 절 최고위원회의</h4> 

                                <h4>제 31 조 (구성) </h4>
                                <p class="ind">① 당무 전반에 관한 심의‧의결기관으로서 당무를 통할‧조정하기 위하여 최고위원회의를 둔다.</p>
                                <p class="ind">
                                    ② 최고위원회의는 다음 각 호의 위원으로 구성한다.<br/>
                                    1. 당 대표<br/>
                                    2. 원내대표<br/>
                                    3. 제27조제1항에 의해 선출된 최고위원 4인<br/>
                                    4. 제27조의2제1항에 의해 선출된 청년최고위원 1인<br/>
                                    5. 당 대표가 최고위원회의의 협의를 거쳐 지명하는 최고위원 1인<br/>
                                    6. 정책위원회 의장
                                </p>
                                <p class="ind">③ 최고위원회의의 의장은 당 대표로 한다.</p> 

                                <h4>제 32 조 (기능) </h4>
                                <p>
                                    최고위원회의는 다음의 기능을 가진다.<br/>
                                    1. 전국위원회와 상임전국위원회 소집 요구<br/>
                                    2. 의원총회 소집 요구<br/>
                                    3. 사무총장 등 최고위원회의의 협의를 요하는 당직자 임명에 대한 협의<br/>
                                    4. 공천관리위원장 등 최고위원회의의 의결을 요하는 당직자 임면에 대한 의결<br/>
                                    5. 국회의원후보자 등 공직후보자의 의결<br/>
                                    6. 당 예산 및 결산과 회계감사에 대한 의결<br/>
                                    7. 전국위원회 또는 의원총회가 회부하는 사항의 심의‧의결<br/>
                                    8. 기타 당무운영에 관한 주요사항의 처리<br/>
                                    9. 기타 주요 당무에 관한 심의‧의결 
                                </p>

                                <h4>제 33 조 (소집 및 의사) </h4>
                                <p class="ind">① 최고위원회의는 주1회 의장이 소집함을 원칙으로 하고 의장이 필요하다고 인정할 때 또는 재적위원 3분의 1 이상의 요구가 있을 때 임시회의를 소집한다.</p>
                                <p class="ind">② 최고위원회의에 관한 기타 필요한 사항은 당규로 정한다.</p>

                                <h4>제 34 조 (의결정족수) </h4>
                                <p>최고위원회의의 의결정족수는 제59조를 준용한다. 다만, 가부동수의 경우에는 당 대표가 결정권을 가진다. </p>

                                <h4 class="sec">제 6 절 당무집행기구</h4> 

                                <h4>제 35 조 (구성) </h4>
                                <p>당무를 집행하기 위하여 중앙당에 중앙사무처, 시‧도당에 시‧도당사무처를 둔다. </p>

                                <h4>제 36 조 (당무집행기구) </h4>
                                <p class="ind">① 중앙사무처에 당의 전략‧조직‧홍보‧인사‧재정을 총괄하는 사무총장과 이를 보좌하는 전략기획부총장, 조직부총장, 홍보본부장(부총장 급)을 두고, 당 대표 직속으로 대변인을 두며, 세부업무 범위는 당규로 정한다.</p>
                                <p class="ind">② 사무총장이 사고로 인하여 직무를 수행할 수 없는 경우, 전략기획부총장, 조직부총장, 홍보본부장 순으로 그 직무를 대행한다.</p>
                                <p class="ind">③ 중앙사무처에 당규가 정하는 바에 따라 필요부서를 둘 수 있다.</p>

                                <h4>제 37 조 (임명) 사</h4>
                                <p>사무총장, 전략기획부총장, 조직부총장, 홍보본부장, 대변인은 당 대표가 최고위원회의의 협의를 거쳐 임명한다.</p>

                                <h4>제 38 조 (사무처당직자인사위원회)</h4>
                                <p class="ind">① 당무집행기구, 원내대책위원회 및 정책위원회 산하 사무처당직자에 대한 인사를 심의하기 위하여 사무총장을 위원장으로 하는 사무처당직자인사위원회를 둔다.</p>
                                <p class="ind">② 사무처당직자는 사무처당직자인사위원회의 심의를 거쳐 사무총장의 추천으로 최고위원회의에 보고한 후 당 대표가 임명한다.</p>
                                <p class="ind">③ 사무처당직자인사위원회의 운영 등 기타 필요한 사항과 부서별 기능, 복무 및 사무처당직자 임면절차에 관한 기타 필요한 사항은 당규로 정한다.</p>

                                <h4 class="sec">제 7 절 윤리위원회</h4>

                                <h4>제 39 조 (구성) </h4>
                                <p class="ind">① 당의 윤리의식 강화와 기강 유지 및 기풍 진작을 위해 윤리위원회를 둔다.</p>
                                <p class="ind">② 윤리위원회는 당 대표가 최고위원회의의 의결을 거쳐 임명하는 9인 이내의 위원으로 구성하며, 위원장을 포함한 위원의 3분의2 이상은 당외 인사로 한다.</p>
                                <p class="ind">③ 윤리위원회에 위원장 1인과 부위원장 약간인을 두며, 위원 중에서 당 대표가 최고위원회의의 의결을 거쳐 임명한다.</p>
