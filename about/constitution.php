                                <p class="ind">④ 제2항의 본문에 의한 최다득표자가 2인 이상인 때에는 재적대의원 과반수가 출석한 전당대회에서 결선투표를 통해 최다득표자중 다수득표자를 후보자로 지명한다.</p>
                                <p class="ind">⑤ 제4항의 경우 최다득표자가 2인 이상인 때에는 재투표를 실시하여 다수득표자를 후보자로 지명한다.</p>
                                <p class="ind">⑥ 대통령후보자의 선출에 관한 기타 필요한 사항은 당규로 정한다.</p>

